<template>
    <div class="uploadBox">
        <h3>{{headerMessage}}</h3>
        <form role="form" enctype="multipart/form-data" @submit.prevent="onSubmit">
            <div class="uploadBoxMain" v-if="!itemsAdded">
                <div class="form-group">
                    <div class="dropArea" @ondragover="onChange" :class="dragging ? 'dropAreaDragging' : ''"
                         @dragenter="dragging=true" @dragend="dragging=false" @dragleave="dragging=false">
                        <h3>{{dropAreaPrimaryMessage}}</h3>
                        <input type="file" id="items" name="items[]" required multiple @change="onChange">
                        <p class="help-block">{{dropAreaSecondaryMessage}}</p>
                    </div>
                </div>
            </div>
            <div class="uploadBoxMain" v-else>
                <p><strong>{{fileNameMessage}}</strong></p>
                <ol>
                    <li v-for="name in itemsNames">{{name}}</li>
                </ol>
                <p><strong>{{fileSizeMessage}}</strong></p>
                <ol>
                    <li v-for="size in itemsSizes">{{size}}</li>
                </ol>
                <p><strong>{{totalFileMessage}}</strong> {{itemsAdded}}</p>
                <p><strong>{{totalUploadSizeMessage}}</strong> {{itemsTotalSize}}</p>
                <v-btn outline @click="removeItems">{{removeFileMessage}}</v-btn>
                <div class="loader" v-if="isLoaderVisible">
                    <div class="loaderImg"></div>
                </div>
            </div>
            <div>
                <v-btn color="primary" outline type="submit"
                        :disabled="itemsAdded < minItems || itemsAdded > maxItems">
                    {{uploadButtonMessage}}
                </v-btn>
                <v-btn type="button" outline @click="removeItems">{{cancelButtonMessage}}
                </v-btn>
            </div>
            <br>
            <div class="successMsg" v-if="successMsg !== ''">{{successMsg}}</div>
            <div class="errorMsg" v-if="errorMsg !== ''">{{fileUploadErrorMessage}}:<br>{{errorMsg}} <br>{{retryErrorMessage}}
            </div>
            <div class="errorMsg" v-if="itemsAdded && itemsAdded < minItems">{{minFilesErrorMessage}}: {{minItems}}.
                <br>{{retryErrorMessage}}
            </div>
            <div class="errorMsg" v-if="itemsAdded && itemsAdded > maxItems">{{maxFilesErrorMessage}}: {{maxItems}}.
                <br>{{retryErrorMessage}}
            </div>
        </form>
    </div>
</template>

<script>
    require('es6-promise').polyfill();
    import axios from 'axios';

    component: {
        axios
    }
    export default {
        props: {
            postURL: {
                type: String,
                required: true
            },
            minItems: {
                type: Number,
                default: 1
            },
            maxItems: {
                type: Number,
                default: 30
            },
            method: {
                type: String,
                default: 'post'
            },
            postMeta: {
                type: [String, Array, Object],
                default: ''
            },
            postData: {
                type: [Object],
                default: () => {
                }
            },
            postHeader: {
                type: [Object],
                default: () => {
                }
            },
            successMessagePath: {
                type: String,
                required: true
            },
            errorMessagePath: {
                type: String,
                required: true
            },
            headerMessage: {
                type: String,
                default: 'إضافة ملفات'
            },
            dropAreaPrimaryMessage: {
                type: String,
                default: 'إسقاط ملفات متعددة هنا'
            },
            dropAreaSecondaryMessage: {
                type: String,
                default: 'أو انقر للتحميل'
            },
            fileNameMessage: {
                type: String,
                default: 'أسماء'
            },
            fileSizeMessage: {
                type: String,
                default: 'الأحجام'
            },
            totalFileMessage: {
                type: String,
                default: 'مجموع الملفات:'
            },
            totalUploadSizeMessage: {
                type: String,
                default: 'إجمالي حجم التحميل:'
            },
            removeFileMessage: {
                type: String,
                default: 'إزالة الملفات'
            },
            uploadButtonMessage: {
                type: String,
                default: 'تحميل'
            },
            cancelButtonMessage: {
                type: String,
                default: 'إلغاء'
            },
            fileUploadErrorMessage: {
                type: String,
                default: 'حدث خطأ'
            },
            minFilesErrorMessage: {
                type: String,
                default: 'الحد الأدنى من الملفات التي يجب إضافتها إلى القائم بالتحميل'
            },
            maxFilesErrorMessage: {
                type: String,
                default: 'الحد الأقصى للملفات التي يمكن إضافتها إلى القائم بالتحميل'
            },
            retryErrorMessage: {
                type: String,
                default: 'يرجى إزالة الملفات وحاول مرة أخرى.'
            },
            httpMethodErrorMessage: {
                type: String,
                default: 'طريقة HTTP هذه غير مسموح بها. الرجاء استخدام طرق "وضع" أو "نشر".'
            },
            showHttpMessages: {
                type: Boolean,
                default: true
            }
        },

        /*
         * The component's data.
         */
        data() {
            return {
                dragging: false,
                items: [],
                itemsAdded: '',
                itemsNames: [],
                itemsSizes: [],
                itemsTotalSize: '',
                formData: '',
                successMsg: '',
                errorMsg: '',
                isLoaderVisible: false,
            }
        },

        methods: {
            // http://scratch99.com/web-development/javascript/convert-bytes-to-mb-kb/
            bytesToSize(bytes) {
                const sizes = ['Bytes', 'KB', 'MB', 'GB', 'TB'];
                if (bytes === 0) return 'n/a';
                let i = parseInt(Math.floor(Math.log(bytes) / Math.log(1024)));
                if (i === 0) return bytes + ' ' + sizes[i];
                return (bytes / Math.pow(1024, i)).toFixed(2) + ' ' + sizes[i];
            },

            onChange(e) {
                this.successMsg = '';
                this.errorMsg = '';
                this.formData = new FormData();
                let files = e.target.files || e.dataTransfer.files;
                this.itemsAdded = files.length;
                let fileSizes = 0;
                for (let x in files) {
                    if (!isNaN(x)) {
                        this.items = e.target.files[x] || e.dataTransfer.files[x];
                        this.itemsNames[x] = files[x].name;
                        this.itemsSizes[x] = this.bytesToSize(files[x].size);
                        fileSizes += files[x].size;
                        this.formData.append('items[]', this.items);
                    }
                }
                this.itemsTotalSize = this.bytesToSize(fileSizes);
            },

            removeItems() {
                this.items = '';
                this.itemsAdded = '';
                this.itemsNames = [];
                this.itemsSizes = [];
                this.itemsTotalSize = '';
                this.dragging = false;
            },

            onSubmit() {
                this.isLoaderVisible = true;

                if ((typeof this.postMeta === 'string' && this.postMeta !== '') ||
                    (typeof this.postMeta === 'object' && Object.keys(this.postMeta).length > 0)) {
                    this.formData.append('postMeta', this.postMeta);
                }

                if (typeof this.postData === 'object' && this.postData !== null && Object.keys(this.postData).length > 0) {
                    for (var key in this.postData) {
                        this.formData.append(key, this.postData[key]);
                    }
                }

                if (this.method === 'put' || this.method === 'post') {
                    axios({method: this.method, url: this.postURL, data: this.formData, headers: this.postHeader})
                        .then((response) => {
                            this.isLoaderVisible = false;
                            this.$emit('uploaded', response);
                            // Show success message
                            if (this.showHttpMessages)
                                this.successMsg = this.successMessagePath;
                            this.removeItems();
                        })
                        .catch((error) => {
                            this.isLoaderVisible = false;
                            this.$emit('upload-failed', response);
                            if (this.showHttpMessages)
                                this.errorMsg = this.errorMessagePath;
                            this.removeItems();
                        });
                } else {
                    if (this.showHttpMessages)
                        this.errorMsg = this.httpMethodErrorMessage;
                    this.removeItems();
                }
            },
        }
    }
</script>

